// 長期連休（お盆・年末年始の非祝日部分）
const LONG_VACATION = new Set([
  // お盆
  '2024-08-13','2024-08-14','2024-08-15','2024-08-16',
  '2025-08-13','2025-08-14','2025-08-15',
  '2026-08-13','2026-08-14','2026-08-15',
  '2027-08-13','2027-08-14','2027-08-15',
  // 年末
  '2024-12-28','2024-12-29','2024-12-30','2024-12-31',
  '2025-12-28','2025-12-29','2025-12-30','2025-12-31',
  '2026-12-28','2026-12-29','2026-12-30','2026-12-31',
])

export function isHoliday(dateStr) {
  return HOLIDAYS.has(dateStr)
}

export function isLongVacation(dateStr) {
  return LONG_VACATION.has(dateStr)
}

// 0=日, 6=土
export function getDayType(dateStr) {
  const d = new Date(dateStr + 'T00:00:00')
  const dow = d.getDay()
  if (dow === 0 || isHoliday(dateStr) || isLongVacation(dateStr)) return 'red'
  if (dow === 6) return 'blue'
  return 'normal'
}
